    private void cmdEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdEliminarActionPerformed
         int i, op;
        op = JOptionPane.showConfirmDialog(this, "Â¿Seguro que desea eliminar a esta Factura ?", "Eliminar", JOptionPane.YES_NO_OPTION);

        ArrayList<Factura> factura = Helper.traerDatos(ruta);
        if (op == JOptionPane.YES_OPTION) {
            i = tblFactura.getSelectedRow();
           factura.remove(i);
            try {
                salida = new ObjectOutputStream(new FileOutputStream(ruta));
            } catch (FileNotFoundException ex) {
                Logger.getLogger(AgregarFactura.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(AgregarFactura.class.getName()).log(Level.SEVERE, null, ex);
            }
            Helper.volcado(salida, factura);
            Helper.llenarTablaFactura(tblFactura, ruta);
            txtNfactura.setText("");
        txtFecha.setText("");
      
      
        cmbPago.setSelectedIndex(0);

        txtNfactura.requestFocusInWindow();
        }

    }//GEN-LAST:event_cmdEliminarActionPerformed

