    private void cmdGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdGuardarActionPerformed

        String fecha, auxEquipo, auxCliente, nfactura, fpago, codigo, cedula;
        int indice, indice2;
        Computadores equipo;
        Cliente cliente;

        fecha = txtFecha.getText();
        nfactura = txtNfactura.getText();
        fpago = cmbPago.getSelectedItem().toString();
        auxEquipo = cmbEquipo.getSelectedItem().toString();
        auxCliente = cmbCliente.getSelectedItem().toString();

          if (txtNfactura.getText().isEmpty()) {
            getToolkit().beep();
            Helper.mensaje(this, "Digite el numero de la Factura", 3);
            txtNfactura.requestFocusInWindow();

        } else {
        indice = auxEquipo.indexOf("-") - 1;
        indice2 = auxCliente.indexOf("-") - 1;
        codigo = auxEquipo.substring(0, indice);
        cedula = auxCliente.substring(0, indice2);
        ArrayList<Factura> facturaModificada;
        equipo = Helper.traerComputadoresCodigo(codigo, rutaP);
        cliente = Helper.traerClienteCedula(cedula, rutaC);
        try {
            if (aux == 0) {

                Factura f = new Factura(fecha, nfactura, fpago, cliente, equipo);

                f.guardar(salida);

            } else {
                facturaModificada = Helper.modificarFactura(rutaF, fecha, nfactura, fpago, cliente, equipo);
                salida = new ObjectOutputStream(new FileOutputStream(rutaF));
                Helper.volcado(salida, facturaModificada);
                aux = 0;
                Helper.mensaje(this, "Factura Actualizada Correctamente!", 1);
            }
        } catch (IOException ex) {
            Logger.getLogger(Agregar.class.getName()).log(Level.SEVERE, null, ex);
        }

        Helper.llenarTablaFactura(tblFactura, rutaF);

        cmbPago.setSelectedIndex(0);

        JButton botonesH[] = {cmdBuscar, cmdCancelar};
        JButton botonesD[] = {cmdEliminar, cmdGuardar};
          }
       
    }//GEN-LAST:event_cmdGuardarActionPerformed

